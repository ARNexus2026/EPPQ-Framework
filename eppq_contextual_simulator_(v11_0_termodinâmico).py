def build_start_graph(N, mode="path", seed=0):
    rng = np.random.default_rng(seed)

    if mode == "path":
        G = nx.path_graph(N)

    elif mode == "cycle":
        G = nx.cycle_graph(N)

    elif mode == "tree":
        G = nx.generators.trees.random_tree(N, seed=seed)

    elif mode == "ws":
        k = 4 if N >= 6 else 2
        if k >= N:
            k = max(2, N - 2)
        G = nx.watts_strogatz_graph(N, k=k, p=0.15, seed=seed)

    elif mode == "erdos":
        p = min(0.15, 3.0 / max(1, N))
        G = nx.erdos_renyi_graph(N, p=p, seed=seed)

    else:
        raise ValueError("START_GRAPH_MODE deve ser: path, cycle, tree, ws ou erdos")

    G = nx.convert_node_labels_to_integers(G)

    if not nx.is_connected(G):
        comps = [list(c) for c in nx.connected_components(G)]
        for i in range(len(comps) - 1):
            a = int(rng.choice(comps[i]))
            b = int(rng.choice(comps[i + 1]))
            G.add_edge(a, b)

    return G

