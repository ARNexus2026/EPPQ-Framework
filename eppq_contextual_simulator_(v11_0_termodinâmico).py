def run_full_battery():
    rows = []
    all_runs = {}

    for seed in SEEDS:
        for T in TEMPERATURES:
            print("\n======================================")
            print(f"RUN | seed={seed} | T={T}")
            print("======================================")

            sim_local, sim_global, sim_two = run_context_comparison(
                N=N,
                D_star=D_star,
                steps=STEPS,
                seed=seed,
                K_moves=K_moves,
                K_fiber_local=K_fiber_local,
                K_fiber_global=K_fiber_global,
                K_fiber_two_layer=K_fiber_two_layer,
                eps_stop=eps_stop,
                L_ctx=L_ctx,
                rho_star=rho_star,
                p_local_add=p_local_add,
                macro_hop=macro_hop,
                macro_rounds=macro_rounds,
                T=T,
                start_graph_mode=START_GRAPH_MODE,
            )

            for mode, sim in [("local", sim_local), ("global", sim_global), ("two_layer", sim_two)]:
                df = sim.to_dataframe().copy()
                metrics = compute_thermal_metrics(df)

                row = {
                    "seed": seed,
                    "T": T,
                    "mode": mode,
                    "n_steps": len(df),
                    "deg_max_final": int(df["deg_max"].iloc[-1]) if len(df) else np.nan,
                    "components_final": int(df["components"].iloc[-1]) if len(df) else np.nan,
                    **metrics,
                }
                rows.append(row)

                all_runs[(seed, T, mode)] = {
                    "sim": sim,
                    "df": df,
                    "metrics": metrics,
                }

    df_results = pd.DataFrame(rows)

    print("\n===== RESULTADOS AGREGADOS =====")
    display(df_results.sort_values(["mode", "T", "seed"]).reset_index(drop=True))

    summary = (
        df_results
        .groupby(["mode", "T"], as_index=False)
        .agg(
            E_mean_tail_mean=("E_mean_tail", "mean"),
            E_mean_tail_std=("E_mean_tail", "std"),
            E_std_tail_mean=("E_std_tail", "mean"),
            uphill_rate_total_mean=("uphill_rate_total", "mean"),
            uphill_rate_tail_mean=("uphill_rate_tail", "mean"),
            tail_slope_mean=("tail_slope", "mean"),
            deg_max_final_mean=("deg_max_final", "mean"),
            components_final_mean=("components_final", "mean"),
        )
    )

    print("\n===== SUMMARY POR MODE E TEMPERATURA =====")
    display(summary.sort_values(["mode", "T"]).reset_index(drop=True))

    mode_order = ["local", "global", "two_layer"]

    plt.figure(figsize=(11, 5))
    for mode in mode_order:
        sub = summary[summary["mode"] == mode].sort_values("T")
        y = sub["E_mean_tail_mean"].to_numpy(dtype=float)
        yerr = sub["E_mean_tail_std"].fillna(0.0).to_numpy(dtype=float)
        plt.errorbar(sub["T"], y, yerr=yerr, marker="o", capsize=4, label=mode)
    plt.xlabel("Temperatura T")
    plt.ylabel("Energia média no platô")
    plt.title("Termalização EPPQ — energia média de equilíbrio vs temperatura")
    plt.grid(True)
    plt.legend()
    plt.show()

    plt.figure(figsize=(11, 5))
    for mode in mode_order:
        sub = summary[summary["mode"] == mode].sort_values("T")
        plt.plot(sub["T"], sub["uphill_rate_tail_mean"], marker="o", label=mode)
    plt.xlabel("Temperatura T")
    plt.ylabel("Taxa média de aceitação de subida")
    plt.title("Exploração térmica — aceitação de movimentos energéticos ascendentes")
    plt.grid(True)
    plt.legend()
    plt.show()

    plt.figure(figsize=(11, 5))
    for mode in mode_order:
        sub = summary[summary["mode"] == mode].sort_values("T")
        plt.plot(sub["T"], sub["E_std_tail_mean"], marker="o", label=mode)
    plt.xlabel("Temperatura T")
    plt.ylabel("Desvio-padrão médio no platô")
    plt.title("Flutuação térmica no regime assintótico")
    plt.grid(True)
    plt.legend()
    plt.show()

    return df_results, summary, all_runs

