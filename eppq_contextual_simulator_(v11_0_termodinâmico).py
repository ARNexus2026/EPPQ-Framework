def compute_thermal_metrics(df):
    if df is None or len(df) == 0:
        return {
            "E_final": np.nan,
            "E_mean_tail": np.nan,
            "E_std_tail": np.nan,
            "tail_slope": np.nan,
            "uphill_rate_total": np.nan,
            "uphill_rate_tail": np.nan,
            "plateau_var_tail": np.nan,
            "plateau_std_tail": np.nan,
            "burn_in": np.nan,
        }

    energies = df["I_eff"].to_numpy(dtype=float)
    tvals = df["t"].to_numpy(dtype=float)

    burn_in = max(1, len(df) // 2)
    tail = df.iloc[burn_in:].copy()

    if len(energies) >= 2:
        dE = np.diff(energies)
        uphill_rate_total = float(np.mean(dE > 1e-12))
    else:
        uphill_rate_total = np.nan

    if len(tail) >= 2:
        dE_tail = np.diff(tail["I_eff"].to_numpy(dtype=float))
        uphill_rate_tail = float(np.mean(dE_tail > 1e-12))
        coeffs = np.polyfit(
            tail["t"].to_numpy(dtype=float),
            tail["I_eff"].to_numpy(dtype=float),
            1
        )
        tail_slope = float(coeffs[0])
    else:
        uphill_rate_tail = np.nan
        tail_slope = np.nan

    return {
        "E_final": float(energies[-1]),
        "E_mean_tail": float(tail["I_eff"].mean()),
        "E_std_tail": float(tail["I_eff"].std(ddof=1)) if len(tail) > 1 else 0.0,
        "tail_slope": tail_slope,
        "uphill_rate_total": uphill_rate_total,
        "uphill_rate_tail": uphill_rate_tail,
        "plateau_var_tail": float(tail["I_eff"].var(ddof=1)) if len(tail) > 1 else 0.0,
        "plateau_std_tail": float(tail["I_eff"].std(ddof=1)) if len(tail) > 1 else 0.0,
        "burn_in": burn_in,
    }

