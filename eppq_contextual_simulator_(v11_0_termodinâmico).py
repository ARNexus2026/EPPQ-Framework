def run_context_comparison(
    N=30,
    D_star=4,
    steps=60,
    seed=7,
    K_moves=50,
    K_fiber_local=10,
    K_fiber_global=1,
    K_fiber_two_layer=5,
    eps_stop=1e-3,
    L_ctx=5,
    rho_star=0.18,
    p_local_add=0.20,
    macro_hop=2,
    macro_rounds=1,
    T=0.0,
    start_graph_mode="path",
):
    base_graph = build_start_graph(N, mode=start_graph_mode, seed=seed)

    sim_local = EPPQContextualSimulator(
        N=N,
        D_star=D_star,
        R_star=2,
        context_mode="local",
        seed=seed,
        K_moves=K_moves,
        K_fiber_local=K_fiber_local,
        K_fiber_global=K_fiber_global,
        K_fiber_two_layer=K_fiber_two_layer,
        eps_stop=eps_stop,
        L_ctx=L_ctx,
        rho_star=rho_star,
        p_local_add=p_local_add,
        macro_hop=macro_hop,
        macro_rounds=macro_rounds,
        start_graph=base_graph,
    )

    sim_global = EPPQContextualSimulator(
        N=N,
        D_star=D_star,
        R_star=2,
        context_mode="global",
        seed=seed,
        K_moves=K_moves,
        K_fiber_local=K_fiber_local,
        K_fiber_global=K_fiber_global,
        K_fiber_two_layer=K_fiber_two_layer,
        eps_stop=eps_stop,
        L_ctx=L_ctx,
        rho_star=rho_star,
        p_local_add=p_local_add,
        macro_hop=macro_hop,
        macro_rounds=macro_rounds,
        start_graph=base_graph,
    )

    sim_two = EPPQContextualSimulator(
        N=N,
        D_star=D_star,
        R_star=2,
        context_mode="two_layer",
        seed=seed,
        K_moves=K_moves,
        K_fiber_local=K_fiber_local,
        K_fiber_global=K_fiber_global,
        K_fiber_two_layer=K_fiber_two_layer,
        eps_stop=eps_stop,
        L_ctx=L_ctx,
        rho_star=rho_star,
        p_local_add=p_local_add,
        macro_hop=macro_hop,
        macro_rounds=macro_rounds,
        start_graph=base_graph,
    )

    print("\n==============================")
    print("RUN: LOCAL CONTEXT")
    print("==============================")
    sim_local.plot_graph("Estado inicial (local)")
    sim_local.run(steps=steps, T=T)
    sim_local.plot_graph(f"Estado final (local) | T={T}")
    sim_local.plot_energy_total()

    print("\n==============================")
    print("RUN: GLOBAL CONTEXT")
    print("==============================")
    sim_global.run(steps=steps, T=T)
    sim_global.plot_graph(f"Estado final (global) | T={T}")
    sim_global.plot_energy_total()

    print("\n==============================")
    print("RUN: TWO-LAYER CONTEXT")
    print("==============================")
    sim_two.run(steps=steps, T=T)
    sim_two.plot_graph(f"Estado final (two-layer) | T={T}")
    sim_two.plot_energy_total()

    df_local = sim_local.to_dataframe()
    df_global = sim_global.to_dataframe()
    df_two = sim_two.to_dataframe()

    plt.figure(figsize=(10, 5))
    plt.plot(df_local["t"], df_local["I_eff"], label="local")
    plt.plot(df_global["t"], df_global["I_eff"], label="global")
    plt.plot(df_two["t"], df_two["I_eff"], label="two-layer")
    plt.xlabel("t")
    plt.ylabel("I_eff")
    plt.title(f"Comparação da energia efetiva (Temperatura T={T})")
    plt.legend()
    plt.grid(True)
    plt.show()

    return sim_local, sim_global, sim_two

